
public class TweetData {

	//The id and text data from tweets json
    public String id;
    public String text;
    
    public String getId()
    {
        return this.id;
    }
    public String getText()
    {
        return this.text;
    }
	public void setId(String id) {
		this.id = id;
	}
	public void setText(String text) {
		this.text = text;
	}
    
    
}